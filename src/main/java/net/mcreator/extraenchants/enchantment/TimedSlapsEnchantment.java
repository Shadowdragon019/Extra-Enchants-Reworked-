
package net.mcreator.extraenchants.enchantment;

import net.minecraftforge.registries.ObjectHolder;

import net.minecraft.inventory.EquipmentSlotType;
import net.minecraft.enchantment.EnchantmentType;
import net.minecraft.enchantment.Enchantment;

import net.mcreator.extraenchants.ExtraenchantsModElements;

@ExtraenchantsModElements.ModElement.Tag
public class TimedSlapsEnchantment extends ExtraenchantsModElements.ModElement {
	@ObjectHolder("extraenchants:timed_slaps")
	public static final Enchantment enchantment = null;
	public TimedSlapsEnchantment(ExtraenchantsModElements instance) {
		super(instance, 40);
	}

	@Override
	public void initElements() {
		elements.enchantments.add(() -> new CustomEnchantment(EquipmentSlotType.MAINHAND).setRegistryName("timed_slaps"));
	}
	public static class CustomEnchantment extends Enchantment {
		public CustomEnchantment(EquipmentSlotType... slots) {
			super(Enchantment.Rarity.RARE, EnchantmentType.WEAPON, slots);
		}

		@Override
		public int getMinLevel() {
			return 1;
		}

		@Override
		public int getMaxLevel() {
			return 3;
		}

		@Override
		public boolean isTreasureEnchantment() {
			return false;
		}

		@Override
		public boolean isCurse() {
			return false;
		}

		@Override
		public boolean isAllowedOnBooks() {
			return true;
		}
	}
}
